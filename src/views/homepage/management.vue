          និងសម្ពាធពីជំងឺធ្ងន់ធ្ងរ។ វាអាចផ្តល់ជាមួយនឹងការព្យាបាលដើម្បីជាសះស្បើយ។
        </p>

        <h3>អ្វីដែលការថែទាំបំប៉នផ្តល់បាន (What Palliative Care Provides):</h3>
        <ul>
